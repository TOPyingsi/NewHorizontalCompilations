    //获取玩家数据
    public static GetPlayerDate(id: number, Level: number): number[] {
        let Date: number[] = [0, 0, 0, 0, 0];
        Date[0] = MTRNX_Constant.PlayerAttack[id];
        Date[1] = MTRNX_Constant.PlayerHp[id];
        Date[2] = MTRNX_Constant.PlayerMp[id];
        Date[3] = MTRNX_Constant.PlayerHprecover[id];
        Date[4] = MTRNX_Constant.PlayerMprecover[id];
        for (let i = 2; i <= Level; i++) {
            for (let j = 0; j < Date.length; j++) {
                Date[j] = (Date[j] * 1.1);
            }
        }
        for (let j = 0; j < Date.length; j++) {
            Date[j] = Number(Date[j].toFixed(0));
        }
        return Date;
    }
    //获取升级价格
    public static GetPlayerPrice(id: number, Level: number): number {
        let price = MTRNX_Constant.PlayerUpPrice[id];
        for (let i = 2; i <= Level; i++) {
            price = price * 1.1;
        }
        price = Number(price.toFixed(0));
        return price;
    }
