// 定义碰撞分组
export enum HXTJB_PHY_GROUP {
    DEFAULT = 1 << 0,      // 1 (二进制: 0000 0001)
    Coin = 1 << 1,      // 1 (二进制: 0000 0001)
    ClickArea = 1 << 10,        // 4 (二进制: 0000 0100)
    Wall = 1 << 2,       // 2 (二进制: 0000 0010)
    ITEM = 1 << 3,         // 8 (二进制: 0000 1000)
    OBSTACLE = 1 << 4      // 16 (二进制: 0001 0000)
}